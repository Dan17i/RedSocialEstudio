package co.edu.uniquindio.redsocial.models.services.interf;
/**
 * Interfaz que define el comportamiento de objetos temáticos.
 * Proporciona un método para obtener el tema asociado al objeto.
 * @author Daniel Jurado, Sebastian Torres y Juan Soto
 * @since 2025-05-17
 * @version 1.0
 */
public interface Tematico {
    /**
     * Obtiene el tema asociado al objeto.
     *
     * @return una cadena que representa el tema del objeto.
     */
    String getTema();
}
